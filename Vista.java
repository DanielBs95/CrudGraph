    private void btnCrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCrearActionPerformed
        // TODO add your handling code here:
        Alumno alumno = new Alumno(
                this.txtNombre.getText(),
                this.txtApellido.getText(),
                this.txtDni.getText(),
                this.txtTelefono.getText());
        lista.add(alumno);
        mostarDatos();

        //Limpiar los textos que se quedan en los textfiles
        limpiar();
    }//GEN-LAST:event_btnCrearActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        for (int i = 0; i < this.lista.size(); i++) {
            if (this.nombre.equals(this.lista.get(i).getNombre())) {
                this.lista.remove(i);
            }
        }
        mostarDatos();
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        // TODO add your handling code here:
        this.txtNombre.setText(this.nombre);
        this.txtApellido.setText(this.apellido);
        this.txtDni.setText(this.identificacion);
        this.txtTelefono.setText(this.telefono);

    }//GEN-LAST:event_btnModificarActionPerformed

    private void btnGuardarModificacionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarModificacionActionPerformed
        // TODO add your handling code here:
        for (int i = 0; i < lista.size(); i++) {
            if (identificacion.equals(lista.get(i).getIdentificacion())) {
                lista.get(i).setNombre(this.txtNombre.getText());
                lista.get(i).setNombre(this.txtApellido.getText());
                lista.get(i).setNombre(this.txtDni.getText());
                lista.get(i).setNombre(this.txtNombre.getText());
            }
        }
        //Limpiar los textos que se quedan en los textfiles
        limpiar();
        mostarDatos();
    }//GEN-LAST:event_btnGuardarModificacionActionPerformed
    private void limpiar() {
        this.txtNombre.setText("");
        this.txtApellido.setText("");
        this.txtDni.setText("");
        this.txtTelefono.setText("");
    }
