    public void mostarDatos() {
        String matriz[][] = new String[this.lista.size()][this.jtblAlumnos.getColumnCount()];

        for (int i = 0; i < this.lista.size(); i++) {
            matriz[i][0] = this.lista.get(i).getNombre();
            matriz[i][1] = this.lista.get(i).getApellido();
            matriz[i][2] = this.lista.get(i).getIdentificacion();
            matriz[i][3] = this.lista.get(i).getTelefono();
        }

        jtblAlumnos.setModel(new javax.swing.table.DefaultTableModel(matriz, new String[]{
            "Nombre", "Apellido", "Dni", "Telefono"
        }));
    }

